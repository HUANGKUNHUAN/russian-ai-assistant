class SimpleNLP:
    def __init__(self):
        self.responses = load_russian_responses()
        self.compound_keys = {
            "как тебя зовут": ["как тебя зовут", "твоё имя", "как имя", "тебя называ"],
            "кто тебя создал": ["кто тебя создал", "кто создал", "твой создатель"],
            "что ты умеешь": ["что ты умеешь", "что можешь", "твои возможности"],
            "как дела": ["как дела", "как жизнь", "как настроение"],
        }

    def get_response(self, text: str) -> str | None:
        if not text:
            return None
        text_lower = text.lower().strip()

        for key in self.responses:
            if text_lower == key:
                return random.choice(self.responses[key])

        for compound, words_list in self.compound_keys.items():
            for phrase in words_list:
                if phrase in text_lower:
                    return random.choice(self.responses[compound])

        for key in self.responses:
            if key in text_lower and len(key) > 2:
                return random.choice(self.responses[key])

        return self._fallback(text_lower)

    def _fallback(self, text: str) -> str:
        fallbacks = [
            "Интересный вопрос. Расскажите подробнее?",
            "Я вас понял. Давайте обсудим это подробнее.",
            "Хорошо. Что вы думаете об этом?",
            "Я внимательно слушаю. Продолжайте!",
            "Отличная мысль! А что ещё вас интересует?",
        ]
        positive_words = ["хорошо", "отлично", "прекрасно", "класс", "супер"]
        negative_words = ["плохо", "грустно", "ужасно", "проблема"]

        if any(w in text for w in positive_words):
            return "Замечательно! Рад, что у вас всё хорошо."
        if any(w in text for w in negative_words):
            return "Мне жаль. Надеюсь, скоро всё наладится."
        return random.choice(fallbacks)
